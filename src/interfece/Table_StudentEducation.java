    public void matrizStudent() throws IOException {

        File file = new File("Students.dat");

        try {
            fileStudent = new Student_File(file);

            arrayStudent = fileStudent.getAllStudents();
            arrayStudentEducation = fileStudent.getEducationStudents(arrayStudent);

        } catch (FileNotFoundException ex) {
            Logger.getLogger(Table_Books.class.getName()).log(Level.SEVERE, null, ex);
        }

        String matriz_Student[][] = new String[arrayStudentEducation.size()][4];

        for (int i = 0; i < arrayStudentEducation.size(); i++) {

            matriz_Student[i][0] = arrayStudentEducation.get(i).getName();
            matriz_Student[i][1] = arrayStudentEducation.get(i).getCode();
            matriz_Student[i][3] = arrayStudentEducation.get(i).getCareer();
            matriz_Student[i][2] = arrayStudentEducation.get(i).getYear();
            ;

        }

        String temp = "";
        //Ordena la matriz de BOOKS
        for (int i = 0; i < matriz_Student.length; i++) {
            for (int j = i + 1; j < matriz_Student.length; j++) {
                if (matriz_Student[i][0].compareToIgnoreCase(matriz_Student[j][0]) > 0) {
                    System.out.println(matriz_Student[i][0] + " - " + matriz_Student[j][0]);
                    for (int k = 0; k < matriz_Student[0].length; k++) {
                        temp = matriz_Student[j][k];
                        matriz_Student[j][k] = matriz_Student[i][k];
                        matriz_Student[i][k] = temp;
                    }
                }
            }
        }

        Table_StudentEducation.setModel(new javax.swing.table.DefaultTableModel(
                matriz_Student,
                new String[]{
                    "Name", "Code", "Career", "Year"
                }
        ));
    }

